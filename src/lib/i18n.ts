/**
 * Get a translation for a given key and locale
 * Falls back to the key itself if not found
 */
export function t(key: string, locale: Locale, params?: Record<string, string | number>): string {
  const translation = translations[locale]?.[key] || translations.ar?.[key] || key;
  if (!params) return translation;
  return Object.entries(params).reduce(
    (result, [paramKey, paramValue]) =>
      result.replace(new RegExp(`{${paramKey}}`, 'g'), String(paramValue)),
    translation
  );
}

/**
 * Get all translation keys for a given locale
 */
export function getTranslations(locale: Locale): Record<string, string> {
  return translations[locale] || translations.ar;
}

/**
 * Check if a translation key exists
 */
export function hasTranslation(key: string, locale: Locale): boolean {
  return key in (translations[locale] || {});
}

/**
 * Get the default direction for a locale
 */
export function getDirection(locale: Locale): 'rtl' | 'ltr' {
  return locale === 'ar' ? 'rtl' : 'ltr';
}

/**
 * Get the opposite locale
 */
export function getOppositeLocale(locale: Locale): Locale {
  return locale === 'ar' ? 'en' : 'ar';
}
